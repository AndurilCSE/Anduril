    private void jBtnBrowseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnBrowseActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setCurrentDirectory(new File(System.getProperty("user.home")));
        fileChooser.setDialogTitle("Select Your Workspace");
        fileChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        //
        // disable the "All files" option.
        //
        fileChooser.setAcceptAllFileFilterUsed(false);
        //    
        if (fileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
              String path = fileChooser.getSelectedFile().toString();
              File file = new File(path);
              if(file.isDirectory()){
                  XMLWriter writer = new XMLWriter();
                  writer.createWorkspaceNode(path, Boolean.toString(false));
                  createContentData();
                  workspacePath = path;
                  jComboWorkspaces.setSelectedItem(workspacePath);
              }
          }
           else {
          System.out.println("No Selection ");
          }
     
    }//GEN-LAST:event_jBtnBrowseActionPerformed

    private void jBtnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnOkActionPerformed
        // TODO add your handling code here:
              String path = workspacePath;
              File file = new File(path);
              if(file.isDirectory()){
                  XMLWriter writer = new XMLWriter();
                  writer.modifyStatus(jCheckBox1.isSelected(), path);
                  StaticData.workspace = path;
                  window.setVisible(false);
                  HomeGUI.main(null);
              }else{
                  JOptionPane.showMessageDialog(null, "It is not directory", "Error ", JOptionPane.ERROR_MESSAGE);
              }
    }//GEN-LAST:event_jBtnOkActionPerformed


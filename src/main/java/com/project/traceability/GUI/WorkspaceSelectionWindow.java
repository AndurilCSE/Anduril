    private void createContentData(){
        XMLReader reader = new XMLReader();
        reader.readWorkspaces();
        jComboWorkspaces.removeAllItems();
        //read workspaces and add to jComboWorkspaces
        Iterator iterator = StaticData.paths.iterator();
        
        while(iterator.hasNext()){
            jComboWorkspaces.addItem(iterator.next().toString());
        }
        
    }
